# ── Analyst takeaway ───────────────────────────────────────────────────────────
st.subheader("Analyst Takeaway")
st.markdown("""
**Three things every Greek banking analyst needs to know about DTCs:**

1. **It's a sovereign credit story embedded in bank capital.**
   DTC holders are ultimately relying on the Greek state to honour the guarantee if banks return to losses.
   The post-IG upgrade CRP compression (1.3% vs 2%+ pre-2023) partly reflects lower DTC tail risk.

2. **The "headline" CET1 ratios overstate capital quality relative to EU peers.**
   A German or French bank with a 15% CET1 ratio has organic, loss-absorbing capital.
   A Greek bank at 15% CET1 where 50% is DTC has materially lower quality capital.
   Adjusting for DTC brings the "organic CET1" of the four banks to ~8–10% — still above SREP floors,
   but the buffer is narrower than the headline suggests.

3. **The clean-up is structural alpha.**
   As DTC amortises and equity compounds, the DTC/equity ratio falls structurally even without
   any new earnings improvement. This is a multi-year re-rating catalyst: as DTC becomes a smaller
   fraction of CET1, the quality discount narrows, and the sector P/B multiple should converge
   toward European peers (currently trading at a 20–30% P/TBV discount to Western EU banks).
""")

