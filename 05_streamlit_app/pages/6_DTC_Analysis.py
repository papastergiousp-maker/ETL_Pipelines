# ── Context ───────────────────────────────────────────────────────────────────
st.subheader("What Are DTCs and Why Do They Matter?")
st.markdown("""
Greek banks hold a large stock of **Deferred Tax Assets (DTAs)** originating from loan-loss provisions
made during the 2010–2015 sovereign and economic crisis, including losses from the **2012 PSI (Private Sector Involvement)**
— the largest sovereign debt restructuring in history.

Greek **Law 4172/2013** converted most of these DTAs into **Deferred Tax Credits (DTCs)**: state-guaranteed
instruments where the Greek government guarantees the bank will receive a cash payment if the DTA
cannot be offset against future taxable profits. This guarantee makes DTCs eligible for inclusion in **CET1 capital**
under CRR Article 39.

**Why this matters for capital quality:**
- DTCs are not generated by the bank's own earnings — they are a state subsidy disguised as capital
- ECB supervisors, credit rating agencies, and institutional investors apply a quality discount to CET1 ratios with high DTC content
- The higher the DTC-to-CET1 ratio, the more a bank's capital adequacy depends on the Greek sovereign's fiscal health
- All four banks have committed to accelerating DTC derecognition from 2041 to **2032–2034** by voluntarily
  deducting DTCs from regulatory capital ahead of the statutory tax-offset schedule
""")
